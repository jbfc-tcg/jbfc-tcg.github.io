import { mkdir, readFile, rm, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

import { chromium } from 'playwright'
import { createServer } from 'vite'

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const outputDir = path.join(rootDir, 'public', 'generated', 'card-faces')
const manifestPath = path.join(outputDir, 'manifest.json')
const generatedModulePath = path.join(rootDir, 'src', 'generated', 'cardFaces.ts')
const generatorPath = '/scripts/card-face-generator.html'

await ensureGeneratedModule()

const server = await createServer({
  root: rootDir,
  logLevel: 'error',
  server: {
    host: '127.0.0.1',
    port: 0,
  },
})

let browser

try {
  await server.listen()

  const baseUrl = server.resolvedUrls?.local[0]
  if (!baseUrl) throw new Error('Failed to start Vite card face generator server.')

  browser = await chromium.launch({ headless: true })
  const page = await browser.newPage({ viewport: { width: 960, height: 1320 }, deviceScaleFactor: 1 })
  page.on('console', (message) => {
    if (message.type() === 'error') console.error(message.text())
  })

  await page.goto(new URL(generatorPath, baseUrl).toString(), { waitUntil: 'load' })
  await page.waitForFunction(() => typeof window.__renderCardFaces === 'function')

  const results = await page.evaluate(() => window.__renderCardFaces())
  await writeResults(results)

  console.log(`Generated ${results.length} card face image(s).`)
} finally {
  await browser?.close()
  await server.close()
}

async function ensureGeneratedModule() {
  try {
    await readFile(generatedModulePath, 'utf8')
  } catch {
    await mkdir(path.dirname(generatedModulePath), { recursive: true })
    await writeFile(generatedModulePath, generatedModule({}), 'utf8')
  }
}

async function writeResults(results) {
  await rm(outputDir, { recursive: true, force: true })
  await mkdir(outputDir, { recursive: true })

  const manifest = {}

  for (const result of results) {
    if (!result?.id || typeof result.dataUrl !== 'string') {
      throw new Error('Card face generator returned an invalid result.')
    }

    const match = result.dataUrl.match(/^data:image\/png;base64,(.+)$/)
    if (!match) {
      throw new Error(`Card face generator returned a non-PNG data URL for ${result.id}.`)
    }

    const fileName = `${slugify(result.id)}.png`
    await writeFile(path.join(outputDir, fileName), Buffer.from(match[1], 'base64'))
    manifest[result.id] = `/generated/card-faces/${fileName}`
  }

  await writeFile(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`, 'utf8')
  await writeFile(generatedModulePath, generatedModule(manifest), 'utf8')
}

function generatedModule(manifest) {
  return `// This file is generated by \`npm run generate:card-faces\`.\nexport const cardFaceImages: Record<string, string> = ${JSON.stringify(manifest, null, 2)}\n`
}

function slugify(value) {
  return String(value)
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9_-]+/g, '-')
    .replace(/^-+|-+$/g, '')
}
